function loadTranslations(lang) {
    let translation = enhanced_translation.en
    let missing = {}

    if (enhanced_translation[lang]) {
        // Overwrite default (english) entry if available in the specified language
        Object.entries(translation).forEach(([key]) => {
            if (enhanced_translation[lang].hasOwnProperty(key) && enhanced_translation[lang][key] !== undefined) {
                translation[key] = enhanced_translation[lang][key]
            } else {
                // Key not translated, add to missing translations
                missing[key] = translation[key]
            }
        });
    }

    if (Object.keys(missing).length > 0) {
        // Translation incomplete
        console.groupCollapsed('%cStadia Enhanced%c ⚙️ - Loading translation "' + lang + '" - ' + Object.keys(translation).length + ' keys, ' + Object.keys(missing).length + ' missing.', enhanced_consoleEnhanced, '')
        console.table(missing)
        console.groupEnd()
    } else {
        // Translation complete
        console.log('%cStadia Enhanced%c ⚙️ - Loading translation "' + lang + '" - ' + Object.keys(translation).length + ' keys.', enhanced_consoleEnhanced, '')
    }

    return translation
}